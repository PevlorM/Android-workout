package com.example.afinal;

class RepItem {

    private String mRepName;
    private int mRepImage;
    public RepItem(String repName, int RepImage) {
        mRepName = repName;
        mRepImage = RepImage;
    }
    public String getrepName() {

        return mRepName;
    }
    public int getRepImage() {

        return mRepImage;
    }
}